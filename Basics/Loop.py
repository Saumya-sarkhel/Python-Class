# For Loop
# Print forword in for loop
for i in range(1, 6):
  print(i)

print("-----")

# Print Backwards in for loop
for i in range(5, 0, -1):
  print(i)

print("-----")

# Print Backwards in for loop
for i in reversed (range(1,6)):
  print(i)

print("-----")


# While Loop Forword
count = 0
while (count < 3):
    count = count + 1
    print("Hello")

print("-----")

# While Loop backward
count = 3
while (count > 0):
    count = count - 1
    print("Hello")
